class EnhancedForDemo
{
	// method to print names of colours
	static void printNames()
	{
		String coloursNames[]= {"Red","Blue","Green","White","Purple","Yellow"};
		
		for(String s : coloursNames)
		
		{
        	System.out.println(s);
		}
	}
	
	// calling main
	public static void main(String args[])
	{
		printNames();
	}
}




